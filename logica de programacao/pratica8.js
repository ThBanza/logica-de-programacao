// AND ( && )

// Declaramos uma variável chamada 'idade' e atribuímos a ela o valor 18
let idade = 18; // (1)

// Declaramos uma variável chamada 'vistoVerificado' e atribuímos a ela o valor false
let vistoVerificado = false; // (2)

// Usamos o operador lógico AND (&&) para combinar duas expressões
let resultado = (idade >= 18) && (vistoVerificado === true); // (3)

// Exibimos o resultado da expressão lógica no console
console.log(resultado); // (4)




